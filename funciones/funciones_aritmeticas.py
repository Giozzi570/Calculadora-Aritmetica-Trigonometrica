# Definición de la función Suma que toma dos argumentos a y b
def Suma(a, b):
    # Calcula la suma de a y b y la asigna a la variable resultado
    resultado = a + b
    # Retorna el valor de resultado
    return resultado

# Definición de la función Resta que toma dos argumentos a y b
def Resta(a, b):
    # Calcula la resta de a y b y la asigna a la variable resultado
    resultado = a - b
    # Retorna el valor de resultado
    return resultado

# Definición de la función Division que toma dos argumentos a y b
def Division(a, b):
    # Calcula la división de a y b y la asigna a la variable resultado
    resultado = a / b
    # Retorna el valor de resultado
    return resultado

# Definición de la función Multiplicacion que toma dos argumentos a y b
def Multiplicacion(a, b):
    # Calcula la multiplicación de a y b y la asigna a la variable resultado
    resultado = a * b
    # Retorna el valor de resultado
    return resultado