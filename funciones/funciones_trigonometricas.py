# Importamos las funciones trigonométricas cos, sin y tan desde el módulo math.
from math import cos, sin, tan

# Definimos una función Cos que llama a la función cos de math.
# Esto permite encapsular la función y darle un nombre más claro o personalizado.
def Cos(num):
    return cos(num)

# Definimos una función Sin que llama a la función sin de math.
# Similar a Cos, encapsula la función sin para un uso más específico.
def Sin(num):
    return sin(num)

# Definimos una función Tan que llama a la función tan de math.
# Encapsula la función tan para mayor claridad o personalización.
def Tan(num):
    return tan(num)